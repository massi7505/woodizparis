export default function OrderButtons() {
  const { settings } = useWoodizStore();
  const buttons = settings.orderButtons || [];
  const active = buttons.filter((b) => b.enabled);
  if (active.length === 0) return null;

  return (
    <div className="flex flex-col gap-3 w-full">
      {active.map((btn) => {
        const isPhone = btn.type === "phone";
        const isDeliveroo = btn.type === "deliveroo";
        const isUber = btn.type === "ubereats";
        const href = btn.url || "#";
        const target = isPhone ? "_self" : "_blank";

        return (
          <a
            key={btn.id}
            href={href}
            target={target}
            rel={isPhone ? undefined : "noopener noreferrer"}
            className="flex items-center justify-between px-5 py-3.5 rounded-xl font-bold text-sm transition-all hover:opacity-90 hover:-translate-y-0.5 hover:shadow-lg active:scale-95"
            style={{ background: btn.bgColor, color: btn.textColor, fontFamily: "Poppins, sans-serif" }}
          >
            <div className="flex items-center gap-3">
              {isPhone && <PhoneIcon />}
              <span>{btn.label}</span>
              {isUber && <UberEatsLogo />}
              {isDeliveroo && <DeliverooLogo />}
            </div>
            <ChevronRight />
          </a>
        );
      })}
    </div>
  );
}
